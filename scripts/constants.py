# ── Virtual Key Code Mapping ─────────────────────────────────────────
# Key name (lowercase) → Win32 virtual key code, used for send_press_key / send_hotkey

VK_CODE_MAP = {
    "backspace": 0x08,
    "tab": 0x09,
    "enter": 0x0D,
    "return": 0x0D,
    "shift": 0x10,
    "ctrl": 0x11,
    "control": 0x11,
    "alt": 0x12,
    "pause": 0x13,
    "capslock": 0x14,
    "esc": 0x1B,
    "escape": 0x1B,
    "space": 0x20,
    "pageup": 0x21,
    "pagedown": 0x22,
    "end": 0x23,
    "home": 0x24,
    "left": 0x25,
    "up": 0x26,
    "right": 0x27,
    "down": 0x28,
    "insert": 0x2D,
    "delete": 0x2E,
    "del": 0x2E,
    "meta": 0x5B,
    "win": 0x5B,
    "cmd": 0x5B,
}

for _index in range(1, 13):
    VK_CODE_MAP["f%s" % _index] = 0x6F + _index
for _char in "abcdefghijklmnopqrstuvwxyz":
    VK_CODE_MAP[_char] = ord(_char.upper())
for _digit in "0123456789":
    VK_CODE_MAP[_digit] = ord(_digit)

